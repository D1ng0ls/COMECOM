                <p id="1"><span style="color: #006; font-weight: bold;"> O que é a COMECOM? </span>
                    <br><br>
                    O COMECOM é um site de "promoções" simples baseado em imagens e textos onde qualquer pessoa ou loja pode postar e compartilhar promoções. 
                    Diferentes páginas são dedicadas a diferentes tópicos, desde eletrônicos até utensílios de petshop. 
                </p>

                <hr style="width: 95%;">

                <p id="2"><span style="color: #006; font-weight: bold;"> O que é a comunidade? </span>
                    <br><br>
                    A comunidade é o local onde pessoas registradas no site podem postar promoções que elas viram durante o seu dia.
                </p>

                <hr style="width: 95%;">

                <p id="3"><span style="color: #006; font-weight: bold;"> Posso fazer compras na COMECOM? </span>
                    <br><br>
                    Não, nosso objetivo é apenas a divulgação de promoções ativas por parte das vendedoras destes produtos, 
                    incentivando o cliente a comprá-los em suas respectivas lojas físicas ou virtuais.
                </p>

                <hr style="width: 95%;">

                <p id="4"><span style="color: #006; font-weight: bold;"> Todos podem anunciar na COMECOM? </span>
                    <br><br>
                    Sim, no entanto, anúncios realizados por pessoas físicas são destinados à aba da comunidade e os realizados por pessoas 
                    jurídicas/empresas são divulgados em suas respectivas categorias. Isso para a maior segurança possível da veracidade dos anúncios exibidos na página.
                </p>

                <hr style="width: 95%;">

                <p id="5"><span style="color: #006; font-weight: bold;"> Preciso estar cadastrado para anunciar na COMECOM? </span>
                    <br><br>
                    Sim. Para a melhor veracidade das promoções apresentadas, solicitamos os dados específicos do usuário para distinguir usuários comuns de empresas 
                    e, consequentemente, fazermos a devida distinção dos respectivos anúncios produzidos por cada um.
                </p>

                <hr style="width: 95%;">

                <p id="6"><span style="color: #006; font-weight: bold;"> Preciso estar cadastrado para navegar na COMECOM? </span>
                    <br><br>
                    Não. Qualquer visitante da página pode visualizar as promoções ativas, apenas será restrito a visualização da comunidade
                    para os usuários que não estiverem logados.
                </p>

                <hr style="width: 95%;">

                <p id="7"><span style="color: #006; font-weight: bold;"> Como eu denuncio postagens? </span>
                    <br><br>
                    Para denunciar uma postagem no COMECOM, basta clicar no botão "denunciar postagem" existente em cada post. Após isso o comentário será
                    inviado para análise.
                </p>

                <hr style="width: 95%;">

                <p id="8"><span style="color: #006; font-weight: bold;"> Como eu posso deletar as minhas postagens? </span>
                    <br><br>
                    KKKK boa pergunta, amigo.
                </p>

                <hr style="width: 95%;">

                <p id="9"><span style="color: #006; font-weight: bold;"> Eu posso contatar um usuário específico? </span>
                    <br><br>
                    Não. A menos que um usuário opte por incluir um endereço de e-mail ou outras informações de contato em sua postagem, 
                    não há como entrar em contato com esse usuário em particular.
                </p>

                <hr style="width: 95%;">

                <p id="10"><span style="color: #006; font-weight: bold;"> O que é considerado uma contribuição positiva? </span>
                    <br><br>
                    Nossa definição de "contribuição positiva" é o envio de postagens substanciais, úteis, amigáveis ​​e bem-humoradas aos fóruns, 
                    juntamente com o upload de imagens e arquivos de qualidade. 
                </p>

                <hr style="width: 95%;">

                <p id="11"><span style="color: #006; font-weight: bold;"> Por que a contribuição é tão importante? </span>
                    <br><br>
                    A contribuição é importante porque é o que impulsiona este site.
                </p>

                <hr style="width: 95%;">

                <p id="12"><span style="color: #006; font-weight: bold;"> Como posso me tornar um bom colaborador? </span>
                    <br><br>
                    É realmente muito fácil. Se você tem algo a acrescentar, se você tiver uma foto de uma promoção interessante, de alta qualidade, pura, rara, etc.: faça o upload.
                </p>

                <hr style="width: 95%;">

                <p id="13"><span style="color: #006; font-weight: bold;"> Como eu posso saber se eu fui banido do site? </span>
                    <br><br>
                    Você não irá mais conseguir realizar o seu login no COMECOM.
                </p>
